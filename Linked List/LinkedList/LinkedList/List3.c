#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>

#define MAX_LIST_SIZE 100 	// 리스트의 최대크기

typedef int element;			// 항목의 정의
typedef struct {
	element array[MAX_LIST_SIZE];	  // 배열 정의
	int size;		  // 현재 리스트에 저장된 항목들의 개수
} ArrayListType;
// 오류 처리 함수
void error(char* message)
{
	fprintf(stderr, "%s\n", message);
	exit(1);
}
// 리스트 초기화 함수
void init(ArrayListType* L)
{
	L->size = 0;
}
// 리스트가 비어 있으면 1을 반환
// 그렇지 않으면 0을 반환
int is_empty(ArrayListType* L)
{
	return L->size == 0;
}
// 리스트가 가득 차 있으면 1을 반환
// 그렇지 많으면 1을 반환
int is_full(ArrayListType* L)
{
	return L->size == MAX_LIST_SIZE;
}

// 리스트 출력
void display(ArrayListType* L)
{
	int i;
	for (i = 0; i < L->size - 1; i++)
		printf("%d->", L->array[i]);
	printf("%d", L->array[L->size - 1]);
	printf("\n");
}
void add(ArrayListType* L, element item)
{
	if (L->size >= MAX_LIST_SIZE) {
		error("리스트 오버플로우");
	}
	L->array[L->size++] = item;

	for (int i = 0; i < L->size; i++) {
		for (int j = 0; j < L->size - (i + 1); j++) {
			if (L->array[j] > L->array[j+1]) {
				element temp = L->array[j + 1];
				L->array[j + 1] = L->array[j];
				L->array[j] = temp;
			}
		}
	}
}

element delete(ArrayListType* L, int pos)
{
	element item;

	if (pos < 0 || pos >= L->size)
		error("위치 오류");
	item = L->array[pos];
	for (int i = pos; i < (L->size - 1); i++)
		L->array[i] = L->array[i + 1];
	L->size--;
	return item;
}

element is_in_list(ArrayListType* L, element item) {

	for (int i = 0; i < L->size; i++) {
		if (L->array[i] == item)
			return 1;
	}

	return 0;
}
void clear(ArrayListType* L) {
	for (int i = 0; i < MAX_LIST_SIZE; i++) {
		L->array[i] = 0;
	}
	L->size = 0;
}

int get_length(ArrayListType* L) {
	return L->size;
}

int main(void)
{
	// ArrayListType를 정적으로 생성하고 ArrayListType를 	
	// 가리키는 포인터를 함수의 매개변수로 전달한다.
	ArrayListType list;

	init(&list);
	for (int i = 5; i > 0; i--) {
		element item = 0;
		scanf("%d", &item);
		add(&list, item);
		display(&list);
	}

	return 0;
}